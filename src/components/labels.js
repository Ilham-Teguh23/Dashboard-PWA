export const getLabel = (key, value) => {
    if (!labels[key]) {
        console.error(`Key '${key}' tidak ditemukan dalam labels.`);
        return "Key tidak valid";
    }

    if (value && labels[key][value]) {
        return labels[key][value];
    }

    return labels[key].default || "Label tidak ditemukan";
};


